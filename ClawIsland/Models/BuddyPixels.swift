          "..####..", "..####..", ".#.##.#.", "........"],
         ["#......#", "#......#", ".######.", "##....##",
          "..####..", "..####..", "#..##..#", "........"],
         ["#......#", "#......#", ".######.", "##....##",
          "..####..", "..####..", ".#.##.#.", "........"]],

        // 3: 国王 — 锯齿王冠交替闪 + 最宽身体
        [["#.#.#.#.", "########", "#.####.#", "########",
          "########", "########", ".######.", "........"],
         [".#.#.#.#", "########", "#.####.#", "########",
          "########", "########", ".######.", "........"],
         ["#.#.#.#.", "########", "#.####.#", "########",
          "########", "########", ".######.", "........"]],

        // 4: 蘑菇 — 超宽帽 + 极细茎（比例独特，无脸）
        [["########", "########", ".######.", "...##...",
          "...##...", "...##...", "..####..", "........"],
         ["########", "#.######", ".######.", "...##...",
          "...##...", "...##...", "..####..", "........"],
         ["########", "#######.", ".######.", "...##...",
          "...##...", "...##...", "..####..", "........"]],

        // 5: 幽灵 — 圆顶 + 锯齿底交替（最后行空 = 漂浮）
        [[".######.", "########", "#.####.#", "########",
          "########", "########", "##.##.##", "........"],
         [".######.", "########", "#.####.#", "########",
          "########", "########", ".##.##.#", "........"],
         [".######.", "########", "#.####.#", "########",
          "########", "########", "##.##.##", "........"]],

