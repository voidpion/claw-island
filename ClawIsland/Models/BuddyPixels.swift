// Canvas 像素精灵数据 — 8 个角色，各 3 帧动画
// 网格：8 列 × 8 行，UInt8 bitmask，bit7 = 最左列，bit0 = 最右列

enum BuddyPixels {
    static let cols  = 8
    static let rows  = 8
    static let count = 8   // 总角色数，buddyIndex % count

    /// frames[characterIndex][frameIndex] → [row0…row7]
    static let frames: [[[UInt8]]] = raw.map { ch in
        ch.map { fr in
            fr.map { row -> UInt8 in
                row.prefix(8).enumerated().reduce(UInt8(0)) { acc, p in
                    p.element == "#" ? acc | (UInt8(0x80) >> UInt8(p.offset)) : acc
                }
            }
        }
    }

    // ──────────────────────────────────────────────────────
    // 原始字符串：[角色][帧][行]，8 字符宽
    // 约定：'#' = 亮，'.' = 暗
